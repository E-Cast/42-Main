#include "libft.h"

int	ft_isalnum(int chr)
{
	if (chr >= '0' && chr <= '9')
		return (1);
	if ((chr >= 'A' && chr <= 'Z') || (chr >= 'a' && chr <= 'z'))
		return (1);
	return (0);
}
